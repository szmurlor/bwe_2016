public class LokacjaTO {
    private String nazwa;
    private String adres;
    private String typLokcji;

    public String getNazwa() {
        return nazwa;
    }

    public void setNazwa(String nazwa) {
        this.nazwa = nazwa;
    }

    public String getAdres() {
        return adres;
    }

    public void setAdres(String adres) {
        this.adres = adres;
    }

    public String getTypLokcji() {
        return typLokcji;
    }

    public void setTypLokcji(String typLokcji) {
        this.typLokcji = typLokcji;
    }
}
